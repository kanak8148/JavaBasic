package co.in.basic;

/**
 * StringClass mein ek simple String ka use dikhaya gaya hai.
 * Is class mein ek String variable `name` ko initialize kiya gaya hai 
 * aur uska value console pe print kiya gaya hai.
 */
public class StringClass {
    
    /**
     * main method program ka entry point hai.
     * Ismein ek String variable `name` declare kiya gaya hai jisme "KANAK" value assign ki gayi hai.
     * Phir, is String ka value print kiya jata hai.
     *
     * @param args Command line arguments (yaha is method mein use nahi ho rahe).
     */
    public static void main(String[] args) {
        
        // String variable `name` ko "KANAK" value assign ki gayi hai
        String name = "KANAK";
        
        // `name` ka value console pe print kiya ja raha hai
        System.out.println(name);
    }
}
